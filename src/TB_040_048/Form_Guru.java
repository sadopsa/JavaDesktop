    private void jTable1_tabguruMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1_tabguruMouseClicked
        int baris = jTable1_tabguru.getSelectedRow();
        if(baris==-1){
            JOptionPane.showMessageDialog(this, "Pilih Data");
        }else{
            jTextField1_NIP.setText((String)jTable1_tabguru.getValueAt(baris, 0));
            jTextField2_NAMA.setText((String)jTable1_tabguru.getValueAt(baris, 1));
            jTextField3_ALAMAT.setText((String)jTable1_tabguru.getValueAt(baris, 2));
            jTextField4_GENDER.setText((String)jTable1_tabguru.getValueAt(baris, 3));
        }
    }//GEN-LAST:event_jTable1_tabguruMouseClicked

