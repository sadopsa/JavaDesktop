     public void tabelguru(){
        DefaultTableModel tabbrg = new DefaultTableModel();
        tabbrg.addColumn("NIP");
        tabbrg.addColumn("Nama");
        tabbrg.addColumn("Alamat");
        tabbrg.addColumn("Gender");
        
        jTable1_tabguru.setModel(tabbrg);
        
        try {
            Statement stat = (Statement)koneksi.getConnection().createStatement();
            ResultSet result = stat.executeQuery("SELECT * FROM GURU ORDER BY NIP ASC");
            while (result.next()){
                tabbrg.addRow(new Object[]{
                    result.getString("NIP"),
                    result.getString("NAMA"),
                    result.getString("ALAMAT"),
                    result.getString("GENDER"),              
                });
                jTable1_tabguru.setModel(tabbrg);
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Data Gagal Ditampilkan");
        }
    }

